# String com espaços extras
favorite_language = ' python '

# Mostrar a string original (com espaços)
print(f"Original: '{favorite_language}'")

# Remover espaço da direita (final)
print(f"rstrip(): '{favorite_language.rstrip()}'")

# Remover espaço da esquerda (início)
print(f"lstrip(): '{favorite_language.lstrip()}'")

# Remover espaço dos dois lados
print(f"strip(): '{favorite_language.strip()}'")
