def operacoes_aritmeticas():
    a = 10
    b = 3
    soma = a + b
    subtracao = a - b
    multiplicacao = a * b
    divisao = a // b
    modulo = a % b

    print("Soma: ", soma)
    print("Subtração: ", subtracao)
    print("Multiplicação: ", multiplicacao)
    print("Divisão: ", divisao)
    print("Modulo: ", modulo)


if __name__ == "__main__":
    operacoes_aritmeticas()
